package org.mafideju.TestSuitTraining.calculator;

public class Calculator {
  public int integerSum(int sum1, int sum2) {
    return sum1+sum2;
  }

  public int integerSubtraction(int minuendo, int subtraendo) {
    return minuendo-subtraendo;
  }

  public int integerMultiplication(int factorA, int factorB) {
    return factorA*factorB;
  }

  public int integerDivision(int dividendo, int divisor) {
    return dividendo/divisor;
  }

  public double doubleSum(double sum1, double sum2) {
    return sum1+sum2;
  }
}
